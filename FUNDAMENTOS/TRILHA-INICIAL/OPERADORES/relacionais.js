/**
 * O resultado dos Operadores Relacionais
 * Sempre será Verdadeiro ou Falso TRUE ou FALSE
 * Maior ou igual: >=
 * Menor ou igual: <=
 * Maior: >
 * Menor: <
 * Igual: == ou ===
 * Diferente: != ou !==
 */

// Usando Literais
console.log('O número 6 > 3 (maior que)?\n R:', 6 > 3)
console.log('O número 6 < 3 (menor que)?\n R:', 6 < 3)
console.log('O número 9 <= 7 (menor ou igual)?\n R:', 9 <= 7)
console.log('O número 3 >= 3 (maior ou igual)?\n R:', 3 >= 3)
console.log('O número 3 != 2 (diferente)?\n R:', 3 != 2)
console.log('O número 3 == 4 (igual)?\n R:', 3 == 4)

// Usando Variáveis
let a = 9
let b = 3

console.log('O número', a, '>', b, '(maior que)?\n R:', a > b)
console.log('O número', a, '>=', b, '(maior ou igual)?\n R:', a >= b)
console.log('O número', a, '!=', b, '(diferente)?\n R:', a != b)
console.log('O número', a, '<', b, '(menor que)?\n R:', a < b)
console.log('O número', a, '>', b, '(maior que)?\n R:', a > b)
console.log('O número', a, '==', b, '(igual)?\n R:', a == b)