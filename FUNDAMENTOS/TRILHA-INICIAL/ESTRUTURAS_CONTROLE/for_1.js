
//! LAÇO FOR
//* O 'Laço for' precisa de 3 parâmetros para poder funcionar
//* 1- Precisa declarar uma variável de cotrofe
//* 2- Precisa de uma expressão TRUE ou FALSE
//* 3- Precisa de um incremento para sair do laço.

// Quantidade determinada de repetições => x10

// Variável de controle / Expressão / incremento
for (let numero = 1; numero <= 10; numero++) {
    console.log('Laço', numero)
}

console.log('========')

// Outra forma de criar um laço for
for (let numero = 0; numero < 10; numero++) {
    console.log('Laço', numero)
}

console.log('Fim!')