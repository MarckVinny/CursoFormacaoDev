
//! LAÇO FOR - DESAFIO
//* Crie um Laço For decrescente
//* 10, 8, 6, 4, 2, 0

// Variável de controle / Expressão / incremento
for (let numero = 10; numero >= 0; numero -= 2) {
    console.log('Laço', numero)
}

//todo: A forma mais comum de se representar um laço é usando
//todo: a letra 'i' (de iteração) na variável que controla o laço.
for (let i = 10; i >= 0; i -= 2) {
    console.log('Laço', i)
}

console.log('Fim!')